#include<iostream>
#include<cmath>
using namespace std;

int main()
{
	cout<<"Checking the ceiling function"<<endl;
	cout<<"\tceil(9.2) is: "<<ceil(9.2)<<endl;
	cout<<"\tceil(-9.8) is: "<<ceil(-9.8)<<endl;
	cout<<endl<<"Checking the floor function"<<endl;
	cout<<"\tfloor(9.2) is: "<<floor(9.2)<<endl;
	cout<<"\tfloor(-9.8) is:"<<floor(-9.8)<<endl;
	cout<<endl<<"Checking the cosine function"<<endl;
	cout<<"\tcos(0.0) is: "<<cos(0.0)<<endl;
	cout<<endl<<"Checking the sine function"<<endl;
	cout<<"\tsin(0.0) is: "<<sin(0.0)<<endl;
	cout<<endl<<"Checking the tangent function"<<endl;
	cout<<"\ttan(0.0) is: "<< tan(0.0)<<endl;
	cout<<endl<<"Checking the exponent function"<<endl;
	cout<<"\texp(1.0) is: "<<exp(1.0)<<endl;
	cout<<"\texp(2.0) is: "<<exp(2.0)<<endl;
	cout<<endl<<"Checking the floating absolute function"<<endl;
	cout<<"\tfabs(5.1) is: "<<fabs(5.1)<<endl;
	cout<<"\tfabs(0.0) is: "<<fabs(0.0)<<endl;
	cout<<"\tfabs(-8.76) is: "<<fabs(-8.76)<<endl;
	cout<<endl<<"Checking the float-modulus function"<<endl;
	cout<<"\tfmod(13.657 , 2.333) is: "<<fmod(13.657 , 2.333)<<endl;
	cout<<endl<<"Checking the logarithm base e function"<<endl;
	cout<<"\tlog(2.718282) is: "<<log(2.718282)<<endl;
	cout<<"\tlog(7.389056) is: "<<log(7.389056)<<endl;
	cout<<endl<<"Checking the log10(x) function *base 10*"<<endl;
	cout<<"\tlog10(10.0) is: "<<log10(10.0)<<endl;
	cout<<"\tlog10(100.0) is: "<<log10(100.0)<<endl;
	cout<<endl<<"Checking the power function pow(a,b)=a^b"<<endl;
	cout<<"\tpow(2,7) is: "<<pow(2,7)<<endl;
	cout<<"\tpow(9, .5) is: "<<pow(9, .5)<<endl;
	cout<<endl<<"Checking the square root function sqrt(x)"<<endl;
	cout<<"\tsqrt(900.0) is: "<<sqrt(900.0)<<endl;
	cout<<"\tsqrt(9.0) is: "<<sqrt(9.0)<<endl;
	return 0;
}